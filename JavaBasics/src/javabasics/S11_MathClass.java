
package javabasics;


public class S11_MathClass {
    public static void main(String[] args) {
        
        System.out.println(Math.abs(-5.4));//absolute value
        System.out.println(Math.ceil(-5.4));// ceiling value
        System.out.println(Math.floor(-5.4));// floor value
        System.out.println(Math.addExact(5,7));// adding two integer
        System.out.println(Math.cos(5));//cos in radian
        System.out.println(Math.sin(5));// sin in radian
        System.out.println(Math.exp(2));//exponent
        System.out.println(Math.max(2,8));// max of two number
        System.out.println(Math.min(2,8));// min of two number 
        System.out.println(Math.pow(2,4));// power
        System.out.println(Math.random());// random number between 0.0 and 1.0
        
    }
    
}
